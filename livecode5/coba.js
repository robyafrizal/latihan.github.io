function rapotAsrama(students) {
  var output = {}
  for(var i = 0; i < students.length; i++) {
    var result = []
    for(var j = 0; j < students.length; j++) {
      var object = {}
      if(students[i].asrama === students[j].asrama) {
        object.nama = students[j].nama
        object.nilai = students[j].nilai
        result.push(object)
        output[students[j].asrama] = result
      }
    }
  }
  return output
}



